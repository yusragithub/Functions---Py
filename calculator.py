def add(a,b):
  return a + b

def subtract(a,b):
  return a - b

def multiply(a,b):
  return a * b

def divide(a,b):
  return a / b

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print(add(num1,num2))
print(subtract(num1,num2))
print(multiply(num1,num2))
print(divide(num1,num2))